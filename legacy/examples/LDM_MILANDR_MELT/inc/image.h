void PrintImage(u32 mas)
{
  u32 i,j;
  u32 adr;

  LCD_CLS();
  CurrentMethod = MET_OR;

  for(i=0;i<8;i++)
  {
    adr = 63-size_x[mas]/2;
    SetCrystal(0);
    LCD_SET_PAGE(i);
    LCD_SET_ADDRESS(adr);

    for(j=0;j<size_x[mas];j++)
    {
      if(adr == 64)
      {
        adr = 0;
        SetCrystal(1);
        LCD_SET_PAGE(i);
        LCD_SET_ADDRESS(adr);
      }

      switch(mas)
      {
        case 0:
              WriteLCD_Data(0xFF-(unsigned char)(BitSwap(LDM_IMAGE[j][i])));        // IMAGE LDM
        break;

        case 1:
              WriteLCD_Data(0xFF-(unsigned char)(BitSwap(MILANDR_IMAGE[j][i])));        // IMAGE MILANDR
        break;

        case 2:
              WriteLCD_Data(0xFF-(unsigned char)(BitSwap(PHYTON_IMAGE[j][i])));        // IMAGE PHYTON
        break;

        case 3:
              WriteLCD_Data(0xFF-(unsigned char)(BitSwap(MELT_IMAGE[j][i])));        // IMAGE MELT
        break;
      }

      adr++;
    }
  }
}


